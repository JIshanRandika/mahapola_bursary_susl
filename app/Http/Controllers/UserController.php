    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $users = DB::select('select * from users');
        try {
            $pro=new User();
            $pro->name = $request->name;
            $pro->email = $request->email;
            $pro->is_permission = $request->is_permission;
            $pro->password = bcrypt($request->password);
            $pro->save();
            return view('user_edit_view',['users'=>$users])
                ->with('success','New user added successfully.');
        }catch (QueryException $e){
            return view('user_edit_view',['users'=>$users])
                ->with('success',"Already registered");
        }
    }

